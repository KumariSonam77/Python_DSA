#append(), insert(), remove(), pop() methods in list
fruits = ["Apple", "Banana"]
fruits.append("Mango")
print(fruits)

numbers = [10, 20, 40]
numbers.insert(1, 30)
print(numbers)

colors = ["Red", "Blue", "Green"]
colors.remove("Blue")
print(colors)

marks = [80, 90, 70, 60]
marks.pop(3)
print(marks)

marks = [80, 90, 70, 60]
marks.remove(80)
print(marks)